// length
document.write('Claudio Lima'.length) // Retorna 12
// charAt()
document.write('Claudio Lima'.charAt(8)) // Retorna 'L'
// indexOf()
var nome = 'Claudio Lima'
document.write(nome.indexOf('a')) // Retorna 2
// replace()
var nome = 'Claudio Lima'
document.write(nome.replace('Lima', 'Silva')) // Retorna 'Claudio Silva'
// substr()
var nome = 'Claudio Lima'
document.write(nome.substr(6, 4)) // Retorna 'o Li'
// toLowerCase()
var nome = 'Claudio Lima'
document.write(nome.toLowerCase()) // Retorna 'claudio lima'